package day2;

public class forEx1 {

	public static void main(String[] args) {
		// for문 기본 예저 : Hello world 5번 출력
		int i;
		for(i=1; i<=5; i++) {
			System.out.println("Hello world");
		}
		for(i=0; i<5; i++) {
			System.out.println("잠을 자고 싶다");
		}
		//for문 기본 예제 : 1부터 5까지 출력하는 예제
		for(i=1; i<=5; i++) {
			System.out.println(i);
		}

	}

}
